package com.grownited.bean;

public class ReviewProductBean {
	Integer reviewproductId;
	Integer userId;
	Integer productId;
	String viewedAt;
	String productName;
	String price;
	
	
	
	public Integer getReviewproductId() {
		return reviewproductId;
	}
	public void setReviewproductId(Integer reviewproductId) {
		this.reviewproductId = reviewproductId;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public String getViewedAt() {
		return viewedAt;
	}
	public void setViewedAt(String viewedAt) {
		this.viewedAt = viewedAt;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
	
	

}
